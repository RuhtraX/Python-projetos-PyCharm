a = input('digite algo: ')
print('o tipo primitivo desse valor é', type(a))
print('\033[31m{}\033[m só tem espaços?'.format(a), a.isspace())
print('\033[32m{}\033[m é um número?'.format(a), a.isnumeric())
print('\033[33m{}\033[m é alfabético?'.format(a), a.isalpha())
print('\033[34m{}\033[m é alfanumérico?'.format(a), a.isalnum())
print('\033[35m{}\033[m está em maiúsculas?'.format(a), a.isupper())
print('\033[36m{}\033[m está em minúsculas?'.format(a), a.islower())
print('\033[37m{}\033[m está capitalizada?'.format(a), a.istitle())